// ─── CDK-generated wildcards (category 2) ───

function suppressCdkWildcards(stack: Stack) {
  NagSuppressions.addStackSuppressions(stack, [
    // CDK S3 grant methods expand to s3:GetObject*, s3:GetBucket*, s3:List*, etc.
    { id: 'AwsSolutions-IAM5', reason: CDK_GRANT, appliesTo: [
      'Action::s3:GetObject*',
      'Action::s3:GetBucket*',
      'Action::s3:List*',
      'Action::s3:Abort*',
      'Action::s3:DeleteObject*',
    ]},
  ]);
}
